public class Operators {
    public static void main(String[] args) {
        int a = 10+10;
        int b = 10-5;
        int c = 10*2;
        int d = 10/2;
        int e = 10%2;

        System.out.println("Arithmetic");
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);

        // relation
        boolean rel1 = 4>3;
        boolean rel2 = 4<3;
        boolean rel3 = 4>=3;
        boolean rel4 = 4<=3;
        boolean rel5 = 4==3;

        System.out.println("Boolean");
        System.out.println(rel1);
        System.out.println(rel2);
        System.out.println(rel3);
        System.out.println(rel4);
        System.out.println(rel5);

        // Logical operators
        boolean log1 = true&&true;
        boolean log2 = true&&false;
        boolean log3 = false&&true;
        boolean log4 = false&&false;

        System.out.println("boolean");
        System.out.println(log1);
        System.out.println(log2);
        System.out.println(log3);
        System.out.println(log4);

        // or
        boolean logor1 = true&&true;
        boolean logor2 = true&&false;
        boolean logor3 = false&&true;
        boolean logor4= false&&false;

        System.out.println("boolean");
        System.out.println(log1);
        System.out.println(log2);
        System.out.println(log3);
        System.out.println(log4);
       




        
    }
}
